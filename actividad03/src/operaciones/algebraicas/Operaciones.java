package operaciones.algebraicas;

/*
Desc: Clase publica que contiene las funciones con operacion algebraicas.
Input: N/A
Output: N/A
*/

public class Operaciones {
    
    /*
    Desc: Funcion que calcula la raiz cuadrada del numero que recibe como parametro.
    Input: Valor como Double.
    Output: Valor como DoubleN/A
    */
        
    public static double muestraRaizCuadrada(double numeroACalcularRaiz){
    
        double resultado_raiz = Math.sqrt(numeroACalcularRaiz);
        
        return resultado_raiz;
       
    }
       
}
